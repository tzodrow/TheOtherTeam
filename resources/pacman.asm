START			ACTi 1 CRT
				LDi 1 SPD 0
				LDi 1 HTH 0
				LDi 1 SCR 0
				LDi 1 LIV 3
				LDi 1 XCRD 2
				LDi 1 YCRD 14
SET				ADDi R4 R0 10000000
WAIT			SUBi R4 R4 1
				B EQ KEYSTROKE
				B UNCON WAIT
KEYSTROKE	  	MOV R6 R27
				RD 1 XCRD R7
				RD 1 YCRD R8
				MOV R11 R7
				MOV R12 R8				
CHECK_LEFT		SUBi R3 R6 11
				B EQ LEFT
CHECK_RIGHT		SUBi R3 R6 7
				B EQ RIGHT
CHECK_UP		SUBi R3 R6 14
				B EQ UP		
CHECK_DOWN		SUBi R3 R6 12
				B EQ DOWN
LEFT			SUBi R7 R7 1
				LD 1 XCRD R7
				B UNCON SET
RIGHT			ADDi R7 R7 1
				LD 1 XCRD R7
				B UNCON SET
UP				SUBi R8 R8 1
				LD 1 YCRD R8
				B UNCON SET
DOWN			ADDi R8 R8 1
				LD 1 YCRD R8
				B UNCON SET